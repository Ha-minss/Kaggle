def top_k_summary(proba, y_true, pct_list=(0.05, 0.10, 0.20)):
    y_true = pd.Series(y_true).reset_index(drop=True)
    proba = np.asarray(proba)

    n = len(proba)
    total_pos = int(y_true.sum())

    rows = []
    for pct in pct_list:
        k = int(np.ceil(n * pct))
        idx = np.argsort(-proba)[:k]  # 상위 k

        tp = int(y_true.iloc[idx].sum())
        fp = int(k - tp)
        precision = tp / k if k > 0 else np.nan
        recall = tp / total_pos if total_pos > 0 else np.nan

        rows.append({
            "Top_%": int(pct * 100),
            "k (Calls)": k,
            "TP (True Yes)": tp,
            "FP (False Yes)": fp,
            "Precision": precision,
            "Recall": recall
        })

    return pd.DataFrame(rows)

