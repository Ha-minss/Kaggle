def best_threshold_by_profit(y_true, proba, revenue=20.0, cost=1.0):
    # threshold 후보를 확률값들로 (혹은 0~1 grid로 해도 됨)
    thresholds = np.unique(np.round(proba, 6))
    best = {"thr": None, "profit": -1e18, "tp": None, "fp": None, "precision": None, "recall": None}

    y_true = np.asarray(y_true)

    for t in thresholds:
        pred = (proba >= t).astype(int)
        tp = ((pred == 1) & (y_true == 1)).sum()
        fp = ((pred == 1) & (y_true == 0)).sum()
        fn = ((pred == 0) & (y_true == 1)).sum()

        profit = tp * revenue - (tp + fp) * cost

        precision = tp / (tp + fp + 1e-12)
        recall = tp / (tp + fn + 1e-12)

        if profit > best["profit"]:
            best = {"thr": float(t), "profit": float(profit), "tp": int(tp), "fp": int(fp),
                    "precision": float(precision), "recall": float(recall)}

    return best

